pub fn make_set_cells() {
    for cell_index in 0..27 as usize {
        let row = cell_index / 9;
        let sqr = (cell_index % 9) / 3;
        let set_cell = ((GROUPS[row] | GROUPS[sqr + 3]) ^ ALL27) | BIT27[cell_index];
        println!("0b{:027b},", set_cell)
    }
}

pub fn make_possibles() {
    let mut possibles: Vec<usize> = vec![ALL27];
    for row_index in 0..3 as usize {
        let mut new_possibles: Vec<usize> = Vec::new();
        for possible in possibles {
            let columns = ((possible & GROUPS[row_index]) >> (row_index * 9)) as usize;
            for col_index in BITS_LISTS.get(columns).unwrap().iter() {
                new_possibles.push(possible & SET_CELLS[row_index * 9 + col_index])
            }
        }
        possibles = new_possibles
    }
    for possible in possibles {
        println!("0b{:027b},", possible)
    }
}

pub fn make_possible_combinations() {
    for &possible1 in POSSIBLES.iter() {
        let mut bit_set = bitset::BitSet::new();
        for (i, &possible2) in POSSIBLES.iter().enumerate() {
            if possible1 & possible2 == 0 {
                bit_set.add(i)
            }
        }
        println!("{},", bit_set.to_string())
    }
}

pub fn make_row_combinations() {
    for &possible1 in POSSIBLES.iter() {
        let mut bit_set = bitset::BitSet::new();
        for (i, &possible2) in POSSIBLES.iter().enumerate() {
            let row1 = possible1 | possible1 >> 9 | possible1 >> 18;
            let row2 = possible2 | possible2 >> 9 | possible2 >> 18;
            if row1 & row2 == 0 {
                bit_set.add(i)
            }
        }
        println!("{},", bit_set.to_string())
    }
}
